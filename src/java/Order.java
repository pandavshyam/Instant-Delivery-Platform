public class Order {
    private String snack;
    private String quantity;


    public void setSnack(String snack) {
        this.snack = snack;
    }

    public String getSnack(){
        return snack;
    }
    
    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getQuantity(){
        return quantity;
    }
}
